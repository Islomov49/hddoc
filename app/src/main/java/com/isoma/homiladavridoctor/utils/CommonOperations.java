    private static byte[] xorWithKey(byte[] left, byte[] right) {
        byte[] a,key;
        if(left.length>right.length){
            a = left;
            key = right;
        }
        else {
            a = right;
            key = left;
        }
        byte[] out = new byte[a.length];
        for (int i = 0; i < a.length; i++) {
            out[i] = (byte) (a[i] ^ key[i%key.length]);
        }
        return out;
    }

    public static String xorToKey(String left, String right) {
        return Base64.encodeToString(xorWithKey(left.getBytes(), right.getBytes()), Base64.URL_SAFE);
    }


